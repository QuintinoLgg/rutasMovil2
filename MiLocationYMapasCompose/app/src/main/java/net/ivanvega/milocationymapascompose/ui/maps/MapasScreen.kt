@Composable
fun MiMapa(){
    val italy = LatLng(42.649153, 13.003204)
    val cameraPositionState = rememberCameraPositionState {
        position = CameraPosition.fromLatLngZoom(italy, 10f)
    }
    GoogleMap(
        modifier = Modifier.fillMaxSize(),
        cameraPositionState = cameraPositionState
    ) {
        Marker(
            state = MarkerState(position = italy),
            title = "Italy",
            snippet = "Marker in Italy"
        )
    }
}

